OTBN_DECLARE_APP_SYMBOLS(p256_ecdh_shared_key);
OTBN_DECLARE_SYMBOL_ADDR(p256_ecdh_shared_key, d0);
OTBN_DECLARE_SYMBOL_ADDR(p256_ecdh_shared_key, d1);
